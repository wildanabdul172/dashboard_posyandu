const applyFilters = (
  usersList: UsersListModel[],
  filters: Filters
): UsersListModel[] => {
  return usersList.filter((usersList) => {
    let matches = true;

    if (filters.status && usersList.status !== filters.status) {
      matches = false;
    }

    return matches;
  });
};

